--RUM－光波追撃
function c3485.initial_effect(c)
	--Activate
	local e1=Effect.CreateEffect(c)
	e1:SetCategory(CATEGORY_SPECIAL_SUMMON)
	e1:SetType(EFFECT_TYPE_ACTIVATE)
	e1:SetCode(EVENT_FREE_CHAIN)
	e1:SetProperty(EFFECT_FLAG_CARD_TARGET)
	e1:SetCondition(c3485.condition)
	e1:SetTarget(c3485.target)
	e1:SetOperation(c3485.activate)
	c:RegisterEffect(e1)
end
function c3485.condition(e,tp,eg,ep,ev,re,r,rp)
	return Duel.GetLP(tp)-Duel.GetLP(1-tp)>=2000 or Duel.GetLP(1-tp)-Duel.GetLP(tp)>=2000
end
function c3485.afilter1(c,e,tp)
	local rk=c:GetRank()
	return rk>0 and c:IsFaceup() and c:IsSetCard(0xe5) and c:IsType(TYPE_XYZ)
		and Duel.IsExistingMatchingCard(c3485.afilter2,tp,LOCATION_EXTRA,0,1,nil,e,tp,c,rk+1)
end
function c3485.afilter2(c,e,tp,mc,rk)
	return c:GetRank()==rk and c:IsSetCard(0xe5) and mc:IsCanBeXyzMaterial(c)
		and c:IsCanBeSpecialSummoned(e,SUMMON_TYPE_XYZ,tp,false,false)
end
function c3485.target(e,tp,eg,ep,ev,re,r,rp,chk,chkc)
	if chkc then return chkc:IsControler(tp) and chkc:IsLocation(LOCATION_MZONE) and c3485.afilter1(chkc,e,tp) end
	if chk==0 then return Duel.GetLocationCount(tp,LOCATION_MZONE)>-1
		and Duel.IsExistingTarget(c3485.afilter1,tp,LOCATION_MZONE,0,1,nil,e,tp) end
	e:SetCategory(CATEGORY_SPECIAL_SUMMON)
	Duel.Hint(HINT_SELECTMSG,tp,HINTMSG_TARGET)
	Duel.SelectTarget(tp,c3485.afilter1,tp,LOCATION_MZONE,0,1,1,nil,e,tp)
	Duel.SetOperationInfo(0,CATEGORY_SPECIAL_SUMMON,nil,1,tp,LOCATION_EXTRA)
end
function c3485.activate(e,tp,eg,ep,ev,re,r,rp)
	local tc=Duel.GetFirstTarget()
	if tc:IsFacedown() or not tc:IsRelateToEffect(e) or tc:IsImmuneToEffect(e) then return end
	Duel.Hint(HINT_SELECTMSG,tp,HINTMSG_SPSUMMON)
	local g=Duel.SelectMatchingCard(tp,c3485.filter2,tp,LOCATION_EXTRA,0,1,1,nil,e,tp,tc,tc:GetRank()+1)
	local sc=g:GetFirst()
	if sc then
		local mg=tc:GetOverlayGroup()
		if mg:GetCount()~=0 then
			Duel.Overlay(sc,mg)
		end
		sc:SetMaterial(Group.FromCards(tc))
		Duel.Overlay(sc,Group.FromCards(tc))
		if Duel.SpecialSummonStep(sc,SUMMON_TYPE_XYZ,tp,tp,false,false,POS_FACEUP) then
			sc:CompleteProcedure()
			local c=e:GetHandler()
			--effect activate
			if sc:GetCode()==18963306 then
				--cipher dragon
				local e1=Effect.CreateEffect(c)
				e1:SetCategory(CATEGORY_CONTROL)
				e1:SetDescription(aux.Stringid(18963306,0))
				e1:SetType(EFFECT_TYPE_SINGLE+EFFECT_TYPE_TRIGGER_O)
				e1:SetCode(EVENT_SPSUMMON_SUCCESS)
				e1:SetProperty(EFFECT_FLAG_CARD_TARGET+EFFECT_FLAG_DAMAGE_STEP+EFFECT_FLAG_DELAY)
				e1:SetCountLimit(1)
				e1:SetRange(LOCATION_MZONE)
				e1:SetCost(c3485.cost1)
				e1:SetTarget(c3485.target1)
				e1:SetOperation(c3485.operation1o)
				e1:SetReset(RESET_EVENT+0x1fe0000)
				sc:RegisterEffect(e1)	
			elseif sc:GetCode()==3488 then
				--cipher dragon anime
				local e1=Effect.CreateEffect(c)
				e1:SetCategory(CATEGORY_CONTROL)
				e1:SetDescription(aux.Stringid(18963306,0))
				e1:SetType(EFFECT_TYPE_SINGLE+EFFECT_TYPE_TRIGGER_O)
				e1:SetCode(EVENT_SPSUMMON_SUCCESS)
				e1:SetProperty(EFFECT_FLAG_CARD_TARGET+EFFECT_FLAG_DAMAGE_STEP+EFFECT_FLAG_DELAY)
				e1:SetCountLimit(1)
				e1:SetRange(LOCATION_MZONE)
				e1:SetCost(c3485.cost1)
				e1:SetTarget(c3485.target1)
				e1:SetOperation(c3485.operation1)
				e1:SetReset(RESET_EVENT+0x1fe0000)
				sc:RegisterEffect(e1)
			elseif sc:GetCode()==2530830 then
				--cipher blade dragon
				local e2=Effect.CreateEffect(c)
				e2:SetDescription(aux.Stringid(2530830,1))
				e2:SetCategory(CATEGORY_DESTROY+EFFECT_FLAG_DAMAGE_STEP+EFFECT_FLAG_DELAY)
				e2:SetType(EFFECT_TYPE_SINGLE+EFFECT_TYPE_TRIGGER_O)
				e2:SetCode(EVENT_SPSUMMON_SUCCESS)
				e2:SetRange(LOCATION_MZONE)
				e2:SetProperty(EFFECT_FLAG_CARD_TARGET)
				e2:SetCountLimit(1)
				e2:SetCost(c3485.descost)
				e2:SetTarget(c3485.destg)
				e2:SetOperation(c3485.desop)
				e2:SetReset(RESET_EVENT+0x1fe0000)
				sc:RegisterEffect(e2)
			elseif sc:GetCode()==12632096 then
				--neo cipher dargon
				local e3=Effect.CreateEffect(c)
				e3:SetCategory(CATEGORY_CONTROL)
				e3:SetDescription(aux.Stringid(12632096,0))
				e3:SetType(EFFECT_TYPE_SINGLE+EFFECT_TYPE_TRIGGER_O)
				e3:SetCode(EVENT_SPSUMMON_SUCCESS)
				e3:SetProperty(EFFECT_FLAG_CARD_TARGET+EFFECT_FLAG_DAMAGE_STEP+EFFECT_FLAG_DELAY)
				e3:SetCountLimit(1)
				e3:SetRange(LOCATION_MZONE)
				e3:SetCondition(c3485.ctcono)
				e3:SetCost(c3485.ctcosto)
				e3:SetTarget(c3485.cttg)
				e3:SetOperation(c3485.ctopo)
				sc:RegisterEffect(e3)
			elseif sc:GetCode()==3489 then
				--neo cipher dargon anime
				local e3=Effect.CreateEffect(c)
				e3:SetCategory(CATEGORY_CONTROL)
				e3:SetDescription(aux.Stringid(12632096,0))
				e3:SetType(EFFECT_TYPE_SINGLE+EFFECT_TYPE_TRIGGER_O)
				e3:SetCode(EVENT_SPSUMMON_SUCCESS)
				e3:SetProperty(EFFECT_FLAG_CARD_TARGET+EFFECT_FLAG_DAMAGE_STEP+EFFECT_FLAG_DELAY)
				e3:SetCountLimit(1)
				e3:SetRange(LOCATION_MZONE)
				e3:SetCondition(c3485.ctcon)
				e3:SetCost(c3485.ctcost)
				e3:SetTarget(c3485.cttg)
				e3:SetOperation(c3485.ctop)
				sc:RegisterEffect(e3)
			end
			Duel.SpecialSummonComplete()
		end
	end
end
function c3485.cost1(e,tp,eg,ep,ev,re,r,rp,chk)
	if chk==0 then return e:GetHandler():CheckRemoveOverlayCard(tp,1,REASON_COST) end
	e:GetHandler():RemoveOverlayCard(tp,1,1,REASON_COST)
end
function c3485.filter1(c)
	return c:IsFaceup() and c:IsControlerCanBeChanged()
end
function c3485.target1(e,tp,eg,ep,ev,re,r,rp,chk,chkc)
	if chkc then return chkc:IsLocation(LOCATION_MZONE) and chkc:IsControler(1-tp) and c3485.filter(chkc) end
	if chk==0 then return Duel.IsExistingTarget(c3485.filter,tp,0,LOCATION_MZONE,1,nil) end
	Duel.Hint(HINT_SELECTMSG,tp,HINTMSG_CONTROL)
	local g=Duel.SelectTarget(tp,c3485.filter,tp,0,LOCATION_MZONE,1,1,nil)
	Duel.SetOperationInfo(0,CATEGORY_CONTROL,g,1,0,0)
end
function c3485.operation1o(e,tp,eg,ep,ev,re,r,rp)
	local c=e:GetHandler()
	local e1=Effect.CreateEffect(c)
	e1:SetType(EFFECT_TYPE_FIELD)
	e1:SetCode(EFFECT_CANNOT_DIRECT_ATTACK)
	e1:SetTargetRange(LOCATION_MZONE,0)
	e1:SetTarget(c3485.atktg)
	e1:SetLabel(e:GetHandler():GetFieldID())
	e1:SetReset(RESET_PHASE+PHASE_END)
	Duel.RegisterEffect(e1,tp)
	local tc=Duel.GetFirstTarget()
	if tc:IsRelateToEffect(e) and Duel.GetControl(tc,tp,PHASE_END,1)~=0 then
		Duel.NegateRelatedChain(tc,RESET_TURN_SET)
		local e2=Effect.CreateEffect(c)
		e2:SetType(EFFECT_TYPE_SINGLE)
		e2:SetCode(EFFECT_DISABLE)
		e2:SetReset(RESET_EVENT+0x1fe0000+RESET_PHASE+PHASE_END)
		tc:RegisterEffect(e2)
		local e3=Effect.CreateEffect(c)
		e3:SetType(EFFECT_TYPE_SINGLE)
		e3:SetCode(EFFECT_DISABLE_EFFECT)
		e3:SetReset(RESET_EVENT+0x1fe0000+RESET_PHASE+PHASE_END)
		tc:RegisterEffect(e3)
		local e4=Effect.CreateEffect(c)
		e4:SetType(EFFECT_TYPE_SINGLE)
		e4:SetCode(EFFECT_SET_ATTACK_FINAL)
		e4:SetValue(3000)
		e4:SetReset(RESET_EVENT+0x1fe0000+RESET_PHASE+PHASE_END)
		tc:RegisterEffect(e4)
		local e5=Effect.CreateEffect(c)
		e5:SetType(EFFECT_TYPE_SINGLE)
		e5:SetCode(EFFECT_CHANGE_CODE)
		e5:SetValue(18963306)
		e5:SetReset(RESET_EVENT+0x1fe0000+RESET_PHASE+PHASE_END)
		tc:RegisterEffect(e5)
	end
end
function c3485.atktg(e,c)
